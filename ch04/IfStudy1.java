package com.green.java.ch04;

public class IfStudy1 {
    public static void main(String[] args) {
        int num = 9;
        //if문은 true이여만 출력함
        if (num == 10) {
            System.out.println("num은 10입니다.");
        } else { //else 나머지라고 생각하세용
            System.out.println("num은 10이 아니다");
        }


    }
}
