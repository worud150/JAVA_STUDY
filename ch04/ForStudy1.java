package com.green.java.ch04;

public class ForStudy1 {
    public static void main(String[] args) {

        for (int i=0; i<5; i++ ) {
            //for ( ; ; ) - 기본적인 구조 (초기화 칸 ; 조건식; 증감식)
            System.out.println(i);
        }
        for (int i=21; i>=18; i--){
            System.out.println("A");
        }


        for (int i=0; i < 5; i++){
            System.out.println(i+1);
        }

        for (int i=5; i < 10; i++){
            System.out.println(i-4);
        }
    }
}
