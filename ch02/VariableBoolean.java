package com.green.java.ch02;

public class VariableBoolean {
    public static void main(String [] args) {

        //결과타입 무조건 true/false
        boolean b1 = true, b2 = false;
        System.out.println(b1);
        System.out.println(b2);

        b1 = 10 < 2;

        System.out.println(b1);
    }
}
